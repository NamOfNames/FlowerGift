const MakeFlower = () => {
  for (let i = 4; i < 10; i++) {
    const rose = document.createElement("div");
    const flower = document.createElement("div");
    const petal = document.createElement("div");
    const petal1 = document.createElement("div");
    const petal2 = document.createElement("div");
    const petal3 = document.createElement("div");
    const petal4 = document.createElement("div");

    const leaf = document.createElement("div");
    const stem = document.createElement("div");
    const leafs = document.createElement("div");
    const leafs1 = document.createElement("div");

    rose.classList.add(`rose-${i}`);
    flower.classList.add(`flower-${i}`);
    petal.classList.add("petal");
    petal1.classList.add("petal");
    petal2.classList.add("petal");
    petal3.classList.add("petal");
    petal4.classList.add("petal");

    leaf.classList.add("leaf");
    stem.classList.add("stem");
    leafs.classList.add("leafs");
    leafs1.classList.add("leafs");

    flower.appendChild(petal);
    flower.appendChild(petal1);
    flower.appendChild(petal2);
    flower.appendChild(petal3);
    flower.appendChild(petal4);

    leaf.appendChild(stem);
    leaf.appendChild(leafs);
    leaf.appendChild(leafs1);

    rose.appendChild(flower);
    rose.appendChild(leaf);
    rose.style.width = `calc(100px + ${i * 10})`;
    rose.style.height = `calc(100px + ${i * 10}`;
    rose.style.position = "absolute";
    rose.style.right = "0";
    rose.style.bottom = "0";
    rose.style.display = "inline-block";
    rose.style.animation = "yidon 5s infinite";
    rose.style.transform = "translateY(40%)";
    document.body.appendChild(rose);
  }
};

